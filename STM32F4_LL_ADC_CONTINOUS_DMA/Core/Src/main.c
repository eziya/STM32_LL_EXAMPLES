/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  LL_APB2_GRP1_EnableClock(LL_APB2_GRP1_PERIPH_SYSCFG);
  LL_APB1_GRP1_EnableClock(LL_APB1_GRP1_PERIPH_PWR);

  /* System interrupt init*/
  NVIC_SetPriorityGrouping(NVIC_PRIORITYGROUP_0);

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */

  // configure DMA source & target
  LL_DMA_ConfigAddresses(
      DMA2,
      LL_DMA_STREAM_0,
      LL_ADC_DMA_GetRegAddr(ADC1, LL_ADC_DMA_REG_REGULAR_DATA),
      (uint32_t)adcVal,
      LL_DMA_DIRECTION_PERIPH_TO_MEMORY);

  // configure DMA length
  LL_DMA_SetDataLength(DMA2, LL_DMA_STREAM_0, 3);

  // enable DMA tranfer complete interrupt
  LL_DMA_EnableIT_TC(DMA2, LL_DMA_STREAM_0);

  // enable DMA stream
  LL_DMA_EnableStream(DMA2, LL_DMA_STREAM_0);

  // enable ADC
  LL_ADC_Enable(ADC1);

  // start conversion
  LL_ADC_REG_StartConversionSWStart(ADC1);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    // check DMA complete
    while(!flagDMATC);

    printfCnt++;

    // convert to temperature & Vdd
    float temp = (float)adcVal[1] * 3.3f / 4096.0f;
    temp = (temp - 0.76) / 0.0025 + 25;

    // Vdd = 3.3V * Vrefint_cal / Vrefint
    float Vdd = 3.3f * (*VREFINT_CAL_ADDR) / (float)adcVal[2];

    // print results
    if(printfCnt > 5000)
    {
      printfCnt = 0;
      printf("ADC CH1: %d, TEMP: %2.1f, Vdd: %1.2f\r\n", adcVal[0], temp, Vdd);
    }

    // clear flag
    flagDMATC = 0;

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

